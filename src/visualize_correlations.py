CACHE_DIR  = "../output/.cache"
CACHE_FILE = os.path.join(CACHE_DIR, "file_hashes.json")

def _load_cache() -> dict:
    """Load the JSON of persisted file hashes from disk."""
    os.makedirs(CACHE_DIR, exist_ok=True)
    if os.path.exists(CACHE_FILE):
        try:
            with open(CACHE_FILE, "r", encoding="utf-8") as f:
                return json.load(f)
        except (json.JSONDecodeError, OSError):
            return {}
    return {}

def _save_cache(cache: dict) -> None:
    """Persist the JSON of file hashes to disk."""
    os.makedirs(CACHE_DIR, exist_ok=True)
    with open(CACHE_FILE, "w", encoding="utf-8") as f:
        json.dump(cache, f, indent=2)

def _file_fingerprint(path: str) -> dict:
    """Return file size in bytes and SHA-256 hex of a file."""
    size = os.path.getsize(path)
    sha  = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            sha.update(chunk)
    return {"size": size, "sha256": sha.hexdigest()}

def needs_processing(path: str, cache: dict) -> bool:
    """
    Return True if the file needs (re)processing.
    Return False if size and SHA-256 match the previous record.
    """
    if not os.path.exists(path):
        return False
    fp = _file_fingerprint(path)
    prev = cache.get(os.path.abspath(path))
    if prev and prev.get("size") == fp["size"] and prev.get("sha256") == fp["sha256"]:
        print(f"[CACHE HIT] {os.path.basename(path)} - unchanged, skipping.")
        return False
    return True

def mark_processed(path: str, cache: dict) -> None:
    """Record the current fingerprint of the file in the cache."""
    if os.path.exists(path):
        cache[os.path.abspath(path)] = _file_fingerprint(path)

