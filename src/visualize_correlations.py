def generate_textual_report(df, csv_file: str, principal_components=None) -> None:
    report_file = csv_file.replace(".csv", "_report.txt")

    G = nx.from_pandas_adjacency(df)
    mask = np.ones(df.shape, dtype=bool)
    np.fill_diagonal(mask, 0)

    corr_pairs = df.where(mask).stack().reset_index()
    corr_pairs.columns = ["Page_A", "Page_B", "Correlation"]
    top_corrs  = corr_pairs.sort_values("Correlation", ascending=False).head(20)
    all_values = corr_pairs["Correlation"].values
    page_means = df.where(mask).mean()

    degree_cent  = nx.degree_centrality(G)
    betweenness  = nx.betweenness_centrality(G)
    communities  = louvain_communities(G, weight="weight", resolution=1, seed=42)
    mod          = modularity(G, communities, weight="weight")
    z_scores_corr = zscore(page_means)
    z_series     = pd.Series(z_scores_corr, index=page_means.index)

    features_path = os.path.join(os.path.dirname(csv_file), "page_features.csv")
    features_df   = None
    if os.path.exists(features_path):
        features_df = pd.read_csv(features_path, index_col=0)

    outliers = z_series[z_series < -2]

    with open(report_file, "w", encoding="utf-8") as f:
        f.write(f"=== ADVANCED STATISTICAL REPORT: {os.path.basename(csv_file)} ===\n\n")
        f.write(f"Overall Mean Correlation: {np.mean(all_values):.4f}\n")
        f.write(f"Modularity (Louvain): {mod:.4f} ")
        if   mod < 0.2: f.write("(Weak Structure)\n")
        elif mod < 0.4: f.write("(Moderate Structure)\n")
        else:           f.write("(Strong Clusters)\n")
        f.write(f"Median: {np.median(all_values):.4f}\n")
        f.write(f"Std Dev: {np.std(all_values):.4f}\n")
        f.write(f"Variance: {np.var(all_values):.4f}\n")
        f.write(f"Max Value (excl. diagonal): {np.max(all_values):.4f}\n")
        f.write(f"Min Value: {np.min(all_values):.4f}\n\n")

        f.write("--- TOP 20 STRONGEST CORRELATIONS ---\n")
        for _, row in top_corrs.iterrows():
            f.write(f"{row['Page_A']} <-> {row['Page_B']}: {row['Correlation']:.6f}\n")

        f.write("\n--- NETWORK CENTRALITY (Bridge/Transit Pages) ---\n")
        for page in sorted(degree_cent, key=degree_cent.get, reverse=True)[:10]:
            f.write(f"{page:10}: Degree={degree_cent[page]:.4f} Betweenness={betweenness[page]:.4f}\n")

        f.write("\n--- DETECTED COMMUNITIES (Louvain) ---\n")
        for i, c in enumerate(communities):
            f.write(f"Cluster {i+1}: {len(c)} páginas ({', '.join(list(c)[:5])}...)\n")

        f.write("\n--- HUB PAGES (High mean similarity with the book) ---\n")
        hub_pages = df.where(mask).mean().sort_values(ascending=False).head(5)
        for page, val in hub_pages.items():
            f.write(f"{page}: Correlação média {val:.4f}\n")

        f.write("\n--- STATISTICAL OUTLIERS (Z-Score < -2) ---\n")
        if outliers.empty:
            f.write("No statistical outliers detected.\n")
        else:
            for page, val in outliers.items():
                f.write(f"{page}: Z={val:.2f}\n")

        z_pca_scores: dict = {}
        if principal_components is not None:
            dist_pca = np.sqrt(
                principal_components[:, 0] ** 2 + principal_components[:, 1] ** 2
            )
            idx_ext = np.argsort(dist_pca)[::-1][:10]
            f.write("\n--- PAGES FAR FROM PCA CENTER (Language anomalies) ---\n")
            for i in idx_ext:
                f.write(f"{df.index[i]:10}: Distância {dist_pca[i]:.3f}\n")
            z_pca_vals = zscore(dist_pca)
            for i, name in enumerate(df.index):
                z_pca_scores[name] = z_pca_vals[i]

        f.write("\n=== TOP SUSPICIOUS PAGES (PRIORITY SCORE) ===\n")
        heuristic_path = os.path.join(os.path.dirname(csv_file), "heuristic_scores.csv")
        h_df = pd.read_csv(heuristic_path, index_col=0) if os.path.exists(heuristic_path) else None

        suspicion_list = []
        for name in df.index:
            z_corr = abs(z_series[name])
            z_pca  = abs(z_pca_scores.get(name, 0))
            cent   = betweenness.get(name, 0)
            score  = (z_corr * 0.2) + (z_pca * 0.4) + (cent * 5.0)

            if features_df is not None and name in features_df.index:
                f_row    = features_df.loc[name]
                ioc_dev  = abs(f_row.get("IoC",     0.034) - 0.067)
                ent_dev  = abs(f_row.get("Entropy", 4.3)   - 4.25)   # noqa: F841
                lrs_bonus = f_row.get("LRS", 0) * 0.5
                score = 0.2 * z_corr + 0.2 * z_pca + 0.3 * ioc_dev + 0.2 * cent + lrs_bonus

            suspicion_list.append((name, score))

        suspicion_list.sort(key=lambda x: x[1], reverse=True)
        for name, score in suspicion_list[:15]:
            f.write(f"{name:12} -> Score: {score:.2f}\n")

        f.write("\n--- INTERNAL STRUCTURE ANALYSIS ---\n")
        high_corr_threshold = 0.8
        dense_count = np.sum(all_values > high_corr_threshold)
        density = dense_count / len(all_values)
        f.write(f"High Correlation Density (>0.8): {density * 100:.2f}%\n")
        f.write(
            "Note: A high density suggests that a large portion of the book "
            "shares the same ciphering algorithm.\n"
        )

    print(f"Textual report saved: {report_file}")

