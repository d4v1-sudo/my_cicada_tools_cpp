# plot_correlation_matrix — heatmap + clustermap + network + PCA
# ---------------------------------------------------------------------------

def plot_correlation_matrix(csv_file: str, cache: dict) -> None:
    if not needs_processing(csv_file, cache):
        return

    if not os.path.exists(csv_file):
        print(f"File not found: {csv_file}")
        return

    df = pd.read_csv(csv_file, index_col=0)
    principal_components = None
    pca = None
    if df.shape[0] > 1:
        pca = PCA(n_components=2)
        principal_components = pca.fit_transform(df)

    generate_textual_report(df, csv_file, principal_components)

    # Matriz de distância cosseno
    dist_mat = squareform(pdist(df.values, metric="cosine"))
    plt.figure(figsize=(12, 10))
    sns.heatmap(dist_mat, cmap="viridis", center=None, square=True)
    plt.title(f"Distance Matrix (Cosine): {os.path.basename(csv_file)}")
    plt.savefig(csv_file.replace(".csv", "_dist_cosine.png"), dpi=300, bbox_inches="tight")
    plt.close()

    sns.set_theme(style="white")

    # Heatmap simples
    plt.figure(figsize=(14, 12))
    sns.heatmap(
        df, cmap="RdYlBu_r", center=0, square=True,
        linewidths=0.5, cbar_kws={"shrink": 0.8},
    )
    plt.title(f"Correlation Matrix: {csv_file}", fontsize=16)
    plt.savefig(csv_file.replace(".csv", "_heatmap.png"), dpi=300, bbox_inches="tight")
    print(f"Heatmap saved: {csv_file.replace('.csv','_heatmap.png')}")
    plt.close()

    # Clustermap hierárquico
    cg = sns.clustermap(  # noqa: F841
        df, cmap="RdYlBu_r", center=0, method="ward", figsize=(15, 15), linewidths=0.5
    )
    plt.title(f"Hierarchical Clustering: {csv_file}", fontsize=16, pad=100)
    plt.savefig(csv_file.replace(".csv", "_cluster.png"), dpi=300, bbox_inches="tight")
    print(f"Clustermap saved: {csv_file.replace('.csv','_cluster.png')}")
    plt.close()

    # Network graph (threshold adaptativo)
    try:
        G = nx.Graph()
        mask_net = np.ones(df.shape, dtype=bool)
        np.fill_diagonal(mask_net, 0)
        all_corr_vals = df.where(mask_net).stack().values
        threshold = float(np.mean(all_corr_vals) + 1.5 * np.std(all_corr_vals))
        threshold = max(0.7, min(threshold, 0.98))

        for i in range(len(df.index)):
            for j in range(i + 1, len(df.columns)):
                if df.iloc[i, j] > threshold:
                    G.add_edge(df.index[i], df.columns[j], weight=df.iloc[i, j])

        if len(G.nodes) > 0:
            plt.figure(figsize=(12, 12))
            pos     = nx.spring_layout(G, k=0.5, iterations=50)
            edges   = G.edges()
            weights = [G[u][v]["weight"] * 2 for u, v in edges]
            nx.draw_networkx_nodes(G, pos, node_size=700, node_color="skyblue", alpha=0.8)
            nx.draw_networkx_edges(G, pos, edgelist=edges, width=weights, edge_color="gray", alpha=0.5)
            nx.draw_networkx_labels(G, pos, font_size=10, font_family="sans-serif")
            plt.title(f"Network of Highly Correlated Pages (>{threshold:.2f})", fontsize=14)
            plt.savefig(csv_file.replace(".csv", "_network.png"), dpi=300, bbox_inches="tight")
            plt.close()
            print(f"Network graph saved: {csv_file.replace('.csv','_network.png')}")
    except Exception as exc:
        print(f"Error generating network graph: {exc}")

    # PCA scatter
    if pca is not None and principal_components is not None:
        try:
            pca_df = pd.DataFrame(
                principal_components,
                columns=["principal_component_1", "principal_component_2"],
            )
            pca_df["Page"] = df.index
            plt.figure(figsize=(12, 10))
            sns.scatterplot(
                x="principal_component_1", y="principal_component_2",
                hue="Page", data=pca_df, s=100,
            )
            plt.title(f"Correlation PCA: {csv_file}", fontsize=16)
            plt.xlabel(f"PC1 ({pca.explained_variance_ratio_[0]*100:.2f}%)")
            plt.ylabel(f"PC2 ({pca.explained_variance_ratio_[1]*100:.2f}%)")
            for _, row in pca_df.iterrows():
                plt.text(
                    row["principal_component_1"] + 0.01,
                    row["principal_component_2"] + 0.01,
                    row["Page"], fontsize=8, ha="left", va="bottom",
                )
            plt.savefig(csv_file.replace(".csv", "_pca.png"), dpi=300, bbox_inches="tight")
            print(f"PCA plot saved: {csv_file.replace('.csv','_pca.png')}")
            plt.close()
        except Exception as exc:
            print(f"Error generating PCA for {csv_file}: {exc}")

    mark_processed(csv_file, cache)

