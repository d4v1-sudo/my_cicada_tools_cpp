def plot_delta_histograms(csv_file: str, cache: dict) -> None:
    if not needs_processing(csv_file, cache):
        return
    if not os.path.exists(csv_file):
        return

    df = pd.read_csv(csv_file)
    if df.empty:
        return

    print(f"\n{'='*60}")
    print(f"--- DEEP DELTA ANALYSIS: {os.path.basename(csv_file)} ---")
    print("=" * 60)

    all_deltas = df["Delta"].tolist()
    max_d      = int(df["Delta"].max())
    fibs       = get_fibonacci(max_d)
    top_deltas = df["Delta"].value_counts().head(10)

    print("\n[1. Mathematical properties of top deltas]")
    for delta, count in top_deltas.items():
        d_int      = int(delta)
        percentage = (count / len(df)) * 100
        props      = []
        if d_int in fibs:     props.append("FIBONACCI")
        if is_prime(d_int):   props.append("PRIME")
        prop_str = f"({', '.join(props)})" if props else ""
        print(f"  Delta {d_int:3d}: {count:3d} times ({percentage:5.2f}%) {prop_str}")

    print("\n[2. Cycle detection (repeating sequences)]")
    for n in range(3, 6):
        repeats = find_repeating_sequences(all_deltas, n)
        if repeats:
            sorted_repeats = sorted(repeats.items(), key=lambda x: x[1], reverse=True)[:5]
            print(f"  Top {n}-grams:")
            for seq, count in sorted_repeats:
                print(f"    {list(seq)} -> {count} occurrences")

    print("\n[3. Suggestion for Skip-Vigenere attack]")
    if len(top_deltas) >= 3:
        suggested = [int(d) for d in top_deltas.index[:5]]
        print(f"  Try using this jump sequence: {suggested}")

    # Histograma por página (se coluna Page existir)
    if "Page" in df.columns:
        for page_name, grp in df.groupby("Page"):
            plt.figure(figsize=(10, 4))
            max_val = int(grp["Delta"].max())
            sns.histplot(data=grp, x="Delta", bins=range(0, max_val + 2), kde=False, color="teal")
            plt.title(f"Deltas — {page_name}", fontsize=13)
            plt.xlabel("Delta")
            plt.ylabel("Occurrences")
            clean = str(page_name).replace(" ", "_")
            out   = csv_file.replace(".csv", f"_hist_{clean}.png")
            plt.savefig(out, dpi=200, bbox_inches="tight")
            plt.close()

    # Histograma global
    plt.figure(figsize=(12, 6))
    sns.histplot(data=df, x="Delta", bins=range(0, max_d + 2), kde=False, color="teal")
    plt.title("Delta Frequency Distribution (Skip Intervals)", fontsize=14)
    plt.xlabel("Delta (distance between interrupts)")
    plt.ylabel("Occurrences")
    out_global = csv_file.replace(".csv", "_histogram.png")
    plt.savefig(out_global, dpi=300, bbox_inches="tight")
    plt.close()
    print(f"\nHistogram saved: {out_global}")

    # TopDelta por página (se coluna TopDelta existir)
    if "TopDelta" in df.columns and "Page" in df.columns:
        top_df = df.sort_values("TopDelta", ascending=False).drop_duplicates("Page")
        plt.figure(figsize=(12, max(5, len(top_df) * 0.35)))
        plt.barh(top_df["Page"].astype(str), top_df["TopDelta"], color="#76b7b2")
        plt.xlabel("TopDelta (largest interval on page)")
        plt.title("Pages with largest interrupt intervals")
        plt.tight_layout()
        out_top = csv_file.replace(".csv", "_topdelta.png")
        plt.savefig(out_top, dpi=200, bbox_inches="tight")
        plt.close()
        print(f"TopDelta plot saved: {out_top}")

    mark_processed(csv_file, cache)

