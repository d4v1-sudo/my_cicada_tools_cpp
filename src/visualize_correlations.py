def is_prime(n: int) -> bool:
    if n < 2:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True

def get_fibonacci(max_val: int) -> list:
    fibs = [1, 2]
    while fibs[-1] < max_val:
        fibs.append(fibs[-1] + fibs[-2])
    return fibs

def find_repeating_sequences(deltas: list, n: int = 3) -> dict:
    """Search for repeating n-grams of deltas."""
    sequences: dict = {}
    for i in range(len(deltas) - n + 1):
        seq = tuple(deltas[i : i + n])
        sequences[seq] = sequences.get(seq, 0) + 1
    return {k: v for k, v in sequences.items() if v > 1}

